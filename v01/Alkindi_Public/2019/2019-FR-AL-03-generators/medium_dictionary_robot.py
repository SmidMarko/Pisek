
from sys import argv
from random import randrange

N=int(argv[1])

def dictionary_robot_generator():
    fr = open("french","r")
    # 97292 french
    n = randrange(1,97292)
    for _ in range(n):
        line = fr.readline()
    return line.strip().lower()




for _ in range(N):
    print (dictionary_robot_generator())
